    private void showDetail() {
        int index = tblPhongBan.getSelectedRow();
        ArrayList<PhongBan> dsPB = PhongBanDAO.layDanhSachPhongBan();
        PhongBan pb = dsPB.get(index);
        txtMaPB.setText(pb.getMaPHG() + "");
        txtTenPB.setText(pb.getTenPHG());
    }
