    private void fillToTable() {
        ArrayList<PhongBan> dsPB = PhongBanDAO.layDanhSachPhongBan();
        DefaultTableModel dtmPhongBan = (DefaultTableModel)tblPhongBan.getModel();
        dtmPhongBan.setRowCount(0);
        for(PhongBan pb: dsPB) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(pb.getMaPHG());
            vec.add(pb.getTenPHG());
            dtmPhongBan.addRow(vec);
        }
//        tblPhongBan.setModel(dtmPhongBan);
    }

